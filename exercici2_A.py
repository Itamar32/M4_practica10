#Este es un diccionario creado de 6 items , cada uno con su key y su value
miDiccionario={'puente':15,'plaza':18,'farola':31,'edificio':15,'quiosco':30,'badulaque':31}
#Esta función te de la longitud del diccionario
print(len(miDiccionario))
#La funcion values te devuelve solo los valores de las keys
print(miDiccionario.values())
#La función popitem te devuelve el último item del diccionario
print(miDiccionario.popitem())